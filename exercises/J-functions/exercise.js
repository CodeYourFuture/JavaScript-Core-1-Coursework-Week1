function halve(number) {
 return number/2;
}

var result = halve(12);
console.log("result 12/2=", result);

var result = halve(24);
console.log("result 24/2=", result);

var result = halve(100);
console.log("result 100/2=", result);

var result = halve(365);

console.log("result 365/2=", result);

//  result 12/2= 6
//  result 24/2= 12
//  result 100/2= 50
//  result 365/2= 182.5